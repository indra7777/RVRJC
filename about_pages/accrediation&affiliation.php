<p>R.V.R. & J.C. College of Engineering is affiliated to AcharyaNagarjuna University (ANU), Nagarjuna Nagar, Guntur
    since it's inception. It is one among the pioneering institute affiliated to AcharyaNagarjuna University (ANU).</p>

   <p> R.V.R. & J.C College strives continuously to meet the standards of AICTE, UGC, APSCHE, NIRF and ISO. So far, the
    College was accredited five times by the National Board of Accreditation (NBA) of AICTE. The college was initially
    accreditation in the year 1998. During the renewal of the accreditation in the year 2002, all the seven departments
    of the college successfully got accredited from NBA. Subsequently during the next phase of renewal in 2007, all the
    seven departments of the college successfully accredited from NBA. In 2012, all the eligible UG Programmes are
    accredited. The college applied for renewal of NBA in 2016 and CSE, IT, ECE, EEE & ME UG courses are accredited in
    the year 2017.</p>

   <p> The institute applied for accreditation by NAAC in the year 2012 and got "A" grade in 2014. The institute applied
    for renewal of accreditation by NAAC in 2019 and got NACC "A+" grade in 2021.</p>

    <p>The college applied for ISO 9001:2015 Certifications and was granted in 2019 from 26.07.2019 to 25.07.2021.</p>

    <p>Apart from the national bodies like NBA, the RVR&JC College of Engineering also got audited by the Andhra Pradesh
    State Council for Higher Education (APSCHE) in 2003.</p>