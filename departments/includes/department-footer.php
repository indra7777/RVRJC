<script>
      const uwidth = document.querySelector(".main").clientWidth/2;
      document.querySelector(".slider-btn").next;
      var slides = document.querySelectorAll(".slider");
      var main = document.querySelector(".main");

      var count = 0;
      var n = slides.length;
      slides.forEach((slide, indx) => {
        slide.style.left = indx * uwidth + "px";

        if (indx < n - 1) {
          nslide = slides[n - indx - 1].cloneNode(true);
          nslide.style.left = -1 * (indx + 1) * uwidth + "px";
          main.insertAdjacentElement("afterbegin", nslide);
        }
      });

      document.querySelectorAll(".slider").forEach((item, indx) => {
              if (indx == 2) {
                item.style.height = 90 + "%";
                // item.style.width = 90 + "%";
              } else {
                item.style.height = 60 + "%";
                // item.style.width = 50 + "%";
              }
            });

      moveSlide = (count) => {
        document.querySelectorAll(".slider").forEach((slide, indx) => {
          slide.style.transform = `translateX(${-1 * count * uwidth}px)`;
        });
      };

      function autoslideFun() {
        autoslide = setInterval(function () {
          document.querySelectorAll(".slider-btn")[1].click();
        }, 3000);
      }
      autoslideFun();

      main.addEventListener("mouseover", function () {
        clearInterval(autoslide);
      });

      main.addEventListener("mouseout", function () {
        autoslideFun();
      });

      document.querySelectorAll(".slider-btn").forEach((btn, indx) => {
        btn.addEventListener("click", function () {
          if (btn.innerText == "prev") {
            clearInterval(autoslide);
            count--;
            document
              .querySelector(".main")
              .removeChild(document.querySelector(".main").lastElementChild);
            temp =
              slides[Math.abs(Math.abs(count) + (n - 1)) % n].cloneNode(true);
            temp.style.left = -1 * (Math.abs(count) + (n - 1)) * uwidth + "px";
            console.log(Math.abs(count) + (n - 1));
            document
              .querySelector(".main")
              .insertAdjacentElement("afterbegin", temp);
            moveSlide(count);
            autoslideFun();
          } else {
            clearInterval(autoslide);
            count++;
            document
              .querySelector(".main")
              .removeChild(document.querySelector(".main").firstElementChild);
            temp = slides[Math.abs(count + (n - 1)) % n].cloneNode(true);

            document.querySelectorAll(".slider").forEach((item, indx) => {
              if (indx == 2) {
                item.style.height = 90 + "%";
                // item.style.width = 60 + "%";
              } else {
                item.style.height = 60 + "%";
                // item.style.width = 50 + "%";
              }
            });

            temp.style.left = (count + (n - 1)) * uwidth + "px";
            document.querySelector(".main").appendChild(temp);
            moveSlide(count);
            autoslideFun();
          }
        });
      });
    </script>

