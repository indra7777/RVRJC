<p align="justify">The culture of an institution is a shared system of values, beliefs and attitudes that shapes and
    enhances behaviour. "The culture is determined through the organization from top to bottom and we must live our
    values to continuously improve." We define our organization culture broadly by six operating Core Principles that
    guide us.</p>
<ol>
    <li><b>Student Focus </b>-The primary focus is not only to educate the students but to inspire them to become
        innovative and contribute to the society.
    </li>
    <li><b>Strengthen Faculty and Staff</b>-The faculty inspire us and direct all we do academically." The staff deliver
        the administrative services and partner with the faculty. "Newer inputs have to be added continuously to the
        capabilities of people.
    </li>
    <li><b>Foster Leadership and ethical decision making</b> -Leadership and ethical decision making are essential for
        the growth of the organization and individual.
    </li>
    <li><b>Committed to accountability and excellence </b>-If we are to remain relevant and attract the best of the
        students and faculty the culture of excellence shall pervade the whole institution both in academic and
        administrative areas.
    </li>
    <li><b>Focus on Resource Management</b> -The financial well-being of an institution is critical for its success."We
        embrace responsibility-centred Management for improving our financial strength.
    </li>
    <li><b>Heighten Reputation</b> -All the units of the institution will gain in many ways through reputation and it is
        everyone's endeavour to add to the reputation.
    </li>
</ol>
<p>While our strategy for action may not directly reflect our core principles, they invariably go into our basic planning &
development processes.</p>

<p>Looking back through the 36 years of journey, there were moments of pride, the notable among them was Accreditation by
NBA in 1998, 2002, 2007, 2012 and 2017; a high rating in Academic Audit & Grading conducted by A.P. State Council of
Higher Education and a place among the Top- Engineering Colleges in national and state surveys and a consistent top
position in university examination results. The institute is under NIRF rank band 201 - 250(for 2020) and ISO 9001:2015
certification from 2019.</p>

<p>It is our endeavour to improve continuously, with the suggestions of our academia, public, Alumni and students, without
abandoning our culture and core principles.</p>