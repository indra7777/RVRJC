
    <b>Mission</b>
<p>The mission of the Alumni Association is to foster a mutually beneficial relationship between the Institute and its
    alumni ... to perpetuate a sense of pride in and commitment to the outstanding qualities of the Institution and the
    education it provides, and to promote, in partnership, a positive image of the Institute and its alumni through
    communication, service and leadership.
</p><b>Objectives</b>
<ol>
    <li>To encourage, foster and promote close relations between the Institution and its alumni and among the alumni
        themselves,</li>
    <li>To promote and encourage friendly relations between all members of the alumni body, an interest in the affairs
        and well-being of the Institution,</li>
    <li>
        To provide and disseminate information regarding their Alma Matter, its graduates, Faculties and students, to
        the alumni,
    </li>
    <li>To initiate and develop programs for the benefit of the alumni,
    </li>
    <li>To assist and support the efforts of the Institution in obtaining funds for development,
    </li>
    <li>To serve as a forum through which alumni may support and advance the pursuit of academic excellence at the
        Institution,
    </li>
    <li>To guide and assist alumni who have recently completed their courses to obtain employment and engage in
        productive
        pursuits useful to society
    </li>
    <li>to organize and co-ordinate reunion activities of the Alumni and let the Alumni acknowledge their gratitude to
        their Alma Matter.
    </li>
    <li>To collect, publish and distribute such information as may be useful to the alumni and their Alma Mater.</li>
</ol>
<p>To open branches of Association in various parts of the country and across the world, and perform all other acts in
    furtherance of the objectives of the Association.</p>
